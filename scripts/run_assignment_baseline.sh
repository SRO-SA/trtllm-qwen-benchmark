#!/bin/bash
set -euo pipefail

source "$(dirname "$0")/common_env.sh"

# Assignment-specific runner for TensorRT-LLM + Qwen3-Coder-480B NVFP4.
# Required assignment scenarios:
#   - Short prompts up to 1k
#   - Medium prompts up to 8k
#   - Long-context evaluation at 32k, 64k, 128k
#   - Multi-user / concurrent workloads
#   - System metrics: TTFT, TPS, concurrency, VRAM, KV growth, runtime stability.
#
# Important real-run fixes:
#   1. Prompt generation is tokenizer-aware; character estimates overshot long prompts.
#   2. TensorRT-LLM PyTorch backend has max_num_tokens separate from max_seq_len.
#   3. Long-context stages keep assignment context lengths but reduce server overhead:
#      smaller max_batch_size, smaller CUDA graph batch_sizes, and enough KV-cache reservation for the requested context window.
#   4. Long-context stages use fewer repeated requests/output tokens so the benchmark
#      does not appear stuck on 64k/128k prefill/generation.
#   5. Long-context stages disable chunked prefill by default because TensorRT-LLM 1.1.0 PyTorch backend can stall around 64k prompts in the chunked-prefill path.
#   6. 64k/128k stages use /v1/completions by default because the chat endpoint can
#      compute a bad default_max_tokens for very long prompts even when server limits
#      are configured correctly.

MODEL_PATH="${MODEL_PATH:-$FINAL_MODEL_PATH}"
MODEL_NAME="${MODEL_NAME:-Qwen3-Coder-480B-A35B-Instruct-NVFP4}"
TP_SIZE="${TP_SIZE:-4}"
QUANTIZATION="${QUANTIZATION:-nvfp4}"
DECODE_MODE="${DECODE_MODE:-baseline}"
OUT="${OUT:-results/assignment_tensorrt_llm_qwen480b_baseline.csv}"

MAX_NEW_TOKENS="${MAX_NEW_TOKENS:-256}"
SAFETY_TOKENS="${SAFETY_TOKENS:-1024}"
PROMPT_TOKEN_RESERVE="${PROMPT_TOKEN_RESERVE:-$SAFETY_TOKENS}"
NUM_REQUESTS="${NUM_REQUESTS:-8}"
KV_DTYPE="${KV_DTYPE:-fp8}"
TIMEOUT_S="${TIMEOUT_S:-1800}"
CASE_TIMEOUT_S="${CASE_TIMEOUT_S:-$((TIMEOUT_S + 300))}"
WAIT_TIMEOUT_S="${WAIT_TIMEOUT_S:-3600}"
LOG_DIR="${LOG_DIR:-results/server_logs}"
METRICS_DIR="${METRICS_DIR:-results/metrics}"
RUNTIME_CONFIG_DIR="${RUNTIME_CONFIG_DIR:-results/runtime_configs}"

# When STRICT_SERVER_LIMITS=1, use the older, more over-provisioned server caps.
# This can be useful for comparison, but it is more likely to OOM on 4x96GB.
STRICT_SERVER_LIMITS="${STRICT_SERVER_LIMITS:-0}"

# Long-context concurrency >1 is optional. Multi-user scalability is covered by 1k/8k.
INCLUDE_32K_CONCURRENCY2="${INCLUDE_32K_CONCURRENCY2:-0}"

mkdir -p results "$LOG_DIR" "$METRICS_DIR" "$RUNTIME_CONFIG_DIR"
touch benchmark/__init__.py 2>/dev/null || true
export PYTHONPATH="$(pwd):${PYTHONPATH:-}"

if [[ "${RESET_RESULTS:-0}" == "1" ]]; then
  rm -f "$OUT"
fi

append_stage_failure_rows() {
  local CONTEXTS="$1"
  local CONCURRENCIES="$2"
  local REASON="$3"
  local STAGE_MAX_NEW_TOKENS="$4"
  local STAGE_NUM_REQUESTS="$5"

  for CONTEXT in $CONTEXTS; do
    for CONCURRENCY in $CONCURRENCIES; do
      "$PYTHON_BIN" scripts/append_failure_row.py \
        --output "$OUT" \
        --model "$MODEL_NAME" \
        --quantization "$QUANTIZATION" \
        --decode-mode "$DECODE_MODE" \
        --context-len "$CONTEXT" \
        --concurrency "$CONCURRENCY" \
        --max-tokens "$STAGE_MAX_NEW_TOKENS" \
        --num-requests "$STAGE_NUM_REQUESTS" \
        --error-message "$REASON"
    done
  done
}

classify_server_failure() {
  local LOG_FILE="$1"
  if [[ ! -f "$LOG_FILE" ]]; then
    echo "server_start_failed_no_log"
    return 0
  fi

  if grep -qiE "CUDA out of memory|out of memory|insufficient GPU memory|Executor creation failed due to insufficient GPU memory" "$LOG_FILE"; then
    echo "server_start_failed_oom"
  elif grep -qiE "Please don't set both cuda_graph_config.batch_sizes and cuda_graph_config.max_batch_size" "$LOG_FILE"; then
    echo "server_start_failed_bad_cuda_graph_config"
  elif grep -qiE "default_max_tokens|splited_prompt_len|max_num_tokens|should not exceed|max_seq_len|max_input_len" "$LOG_FILE"; then
    echo "server_failed_or_stalled_token_limit"
  elif grep -qiE "Traceback|RuntimeError|ValueError" "$LOG_FILE"; then
    echo "server_start_failed_runtime_error"
  else
    echo "server_start_failed_or_timeout_${WAIT_TIMEOUT_S}s"
  fi
}

csv_list() {
  local values="$1"
  local out=""
  for v in $values; do
    if [[ -z "$out" ]]; then out="$v"; else out="$out, $v"; fi
  done
  echo "$out"
}

write_stage_config() {
  local STAGE_NAME="$1"
  local KV_FRAC="$2"
  local MAX_BATCH_SIZE_STAGE="$3"
  local CUDA_BATCHES="$4"
  local ENABLE_CHUNKED_PREFILL_STAGE="$5"
  local STAGE_CONFIG="$6"
  local SERVER_SEQ_LEN_STAGE="$7"
  local SERVER_NUM_TOKENS_STAGE="$8"
  local SERVER_INPUT_LEN_STAGE="${9:-$SERVER_SEQ_LEN_STAGE}"

  local CUDA_BATCHES_CSV
  CUDA_BATCHES_CSV="$(csv_list "$CUDA_BATCHES")"

  cat > "$STAGE_CONFIG" <<EOF_CONFIG
enable_iter_perf_stats: true

# Stage-generated config. Do not edit manually; generated by run_assignment_baseline.sh.
# Keep assignment context lengths unchanged; these values are TensorRT-LLM capacity knobs.
# Important: TensorRT-LLM 1.1.0 may not expose --max_input_len in trtllm-serve,
# so max_input_len must be supplied here. Without this, long prompts can be accepted
# with HTTP 200 but stall internally with a warning like:
#   default_max_tokens (...) = max_seq_len (~33k) - splited_prompt_len (~64k)

max_input_len: ${SERVER_INPUT_LEN_STAGE}
max_seq_len: ${SERVER_SEQ_LEN_STAGE}
max_num_tokens: ${SERVER_NUM_TOKENS_STAGE}
max_batch_size: ${MAX_BATCH_SIZE_STAGE}

enable_chunked_prefill: ${ENABLE_CHUNKED_PREFILL_STAGE}

kv_cache_config:
  free_gpu_memory_fraction: ${KV_FRAC}

# Build config mirrors the top-level capacity settings. This is intentional:
# TensorRT-LLM's PyTorch backend logs both TorchLlmArgs and BuildConfig fields.
build_config:
  max_input_len: ${SERVER_INPUT_LEN_STAGE}
  max_seq_len: ${SERVER_SEQ_LEN_STAGE}
  max_num_tokens: ${SERVER_NUM_TOKENS_STAGE}
  max_batch_size: ${MAX_BATCH_SIZE_STAGE}

cuda_graph_config:
  batch_sizes: [${CUDA_BATCHES_CSV}]
  enable_padding: false
EOF_CONFIG
}

run_stage_once() {
  local STAGE_NAME="$1"
  local SERVER_SEQ_LEN="$2"
  local SERVER_NUM_TOKENS="$3"
  local CONTEXTS="$4"
  local CONCURRENCIES="$5"
  local MAX_BATCH_SIZE_STAGE="$6"
  local CUDA_BATCHES="$7"
  local KV_FRAC="$8"
  local ENABLE_CHUNKED_PREFILL_STAGE="$9"
  local STAGE_NUM_REQUESTS="${10:-$NUM_REQUESTS}"
  local STAGE_MAX_NEW_TOKENS="${11:-$MAX_NEW_TOKENS}"
  local STAGE_FIRST_TOKEN_TIMEOUT_S="${12:-${FIRST_TOKEN_TIMEOUT_S:-$TIMEOUT_S}}"
  local STAGE_OPENAI_API_MODE="${13:-${OPENAI_API_MODE:-chat}}"

  local STAGE_OUT_PLAN="results/plan_assignment_${STAGE_NAME}.json"
  local STAGE_LOG="${LOG_DIR}/server_${STAGE_NAME}.log"
  local STAGE_METRICS="${METRICS_DIR}/metrics_${STAGE_NAME}.json"
  local STAGE_CONFIG="${RUNTIME_CONFIG_DIR}/config_${STAGE_NAME}.yaml"
  local STAGE_STATUS=0

  local MAX_CONTEXT=0
  for C in $CONTEXTS; do
    if (( C > MAX_CONTEXT )); then MAX_CONTEXT=$C; fi
  done
  local REQUIRED_TOTAL_TOKENS=$((MAX_CONTEXT + STAGE_MAX_NEW_TOKENS + SAFETY_TOKENS))

  echo "============================================================"
  echo "Assignment stage: ${STAGE_NAME}"
  echo "Model path: ${MODEL_PATH}"
  echo "Assignment contexts: ${CONTEXTS}"
  echo "Concurrencies: ${CONCURRENCIES}"
  echo "Stage max new tokens: ${STAGE_MAX_NEW_TOKENS}"
  echo "Stage num requests: ${STAGE_NUM_REQUESTS}"
  echo "First-token/read timeout: ${STAGE_FIRST_TOKEN_TIMEOUT_S}"
  echo "OpenAI API mode: ${STAGE_OPENAI_API_MODE}"
  echo "Server max seq len: ${SERVER_SEQ_LEN}"
  echo "Server max num tokens: ${SERVER_NUM_TOKENS}"
  echo "Required total tokens: ${REQUIRED_TOTAL_TOKENS}"
  echo "Max batch size: ${MAX_BATCH_SIZE_STAGE}"
  echo "CUDA graph batch sizes: ${CUDA_BATCHES}"
  echo "KV memory fraction: ${KV_FRAC}"
  echo "Chunked prefill: ${ENABLE_CHUNKED_PREFILL_STAGE}"
  echo "Output: ${OUT}"
  echo "Server log: ${STAGE_LOG}"
  echo "Stage config: ${STAGE_CONFIG}"
  echo "============================================================"

  write_stage_config "$STAGE_NAME" "$KV_FRAC" "$MAX_BATCH_SIZE_STAGE" "$CUDA_BATCHES" "$ENABLE_CHUNKED_PREFILL_STAGE" "$STAGE_CONFIG" "$SERVER_SEQ_LEN" "$SERVER_NUM_TOKENS" "$SERVER_SEQ_LEN"

  bash scripts/stop_trtllm_server.sh || true

  MAX_SEQ_LEN="$SERVER_SEQ_LEN" \
  MAX_NUM_TOKENS="$SERVER_NUM_TOKENS" \
  MAX_INPUT_LEN="$SERVER_SEQ_LEN" \
  MAX_BATCH_SIZE="$MAX_BATCH_SIZE_STAGE" \
  TP_SIZE="$TP_SIZE" \
  MODEL_PATH="$MODEL_PATH" \
  CONFIG_PATH="$STAGE_CONFIG" \
  nohup bash scripts/serve_final_baseline.sh > "$STAGE_LOG" 2>&1 &

  echo $! > server.pid
  echo "Started server PID $(cat server.pid). Log: ${STAGE_LOG}"

  if ! TIMEOUT_S="$WAIT_TIMEOUT_S" SERVER_LOG="$STAGE_LOG" bash scripts/wait_for_server.sh; then
    local REASON
    REASON="$(classify_server_failure "$STAGE_LOG")"
    echo "WARNING: Stage ${STAGE_NAME} server did not become healthy. Reason: ${REASON}. Recording failure rows and continuing."
    SERVER_LOG="$STAGE_LOG" bash scripts/diagnose_server.sh || true
    bash scripts/stop_trtllm_server.sh || true
    if [[ "${SUPPRESS_STAGE_FAILURE_ROWS:-0}" == "1" ]]; then
      echo "Suppressed failure row for retry attempt: ${REASON}"
      return 20
    fi
    append_stage_failure_rows "$CONTEXTS" "$CONCURRENCIES" "$REASON" "$STAGE_MAX_NEW_TOKENS" "$STAGE_NUM_REQUESTS"
    echo "Completed failed stage ${STAGE_NAME}."
    return 0
  fi

  if ! "$PYTHON_BIN" scripts/check_server_limits.py \
      --log "$STAGE_LOG" \
      --required-seq-len "$REQUIRED_TOTAL_TOKENS" \
      --required-num-tokens "$REQUIRED_TOTAL_TOKENS"; then
    echo "WARNING: Stage ${STAGE_NAME} server limits are insufficient. Recording failure rows and continuing."
    SERVER_LOG="$STAGE_LOG" bash scripts/diagnose_server.sh || true
    bash scripts/stop_trtllm_server.sh || true
    if [[ "${SUPPRESS_STAGE_FAILURE_ROWS:-0}" == "1" ]]; then
      echo "Suppressed failure row for retry attempt: server_limits_insufficient_required_${REQUIRED_TOTAL_TOKENS}"
      return 21
    fi
    append_stage_failure_rows "$CONTEXTS" "$CONCURRENCIES" "server_limits_insufficient_required_${REQUIRED_TOTAL_TOKENS}" "$STAGE_MAX_NEW_TOKENS" "$STAGE_NUM_REQUESTS"
    echo "Completed failed stage ${STAGE_NAME}."
    return 0
  fi

  # Validate actual KV-cache window from TensorRT-LLM logs before sending long-context requests.
  # This avoids the failure mode where the server starts but can only allocate a ~33K KV
  # window for a 64K/128K prompt, causing a request to be accepted with HTTP 200 but never
  # make GPU compute progress.
  if [[ "${REQUIRE_KV_WINDOW_CHECK:-0}" == "1" || "$STAGE_NAME" == long_* ]]; then
    if grep -q "window size=" "$STAGE_LOG"; then
      if ! "$PYTHON_BIN" scripts/check_kv_window.py           --log "$STAGE_LOG"           --required-tokens "$REQUIRED_TOTAL_TOKENS"; then
        local REASON="kv_cache_window_too_small_required_${REQUIRED_TOTAL_TOKENS}"
        echo "WARNING: Stage ${STAGE_NAME} KV-cache window is insufficient. Recording failure rows and continuing."
        SERVER_LOG="$STAGE_LOG" bash scripts/diagnose_server.sh || true
        bash scripts/stop_trtllm_server.sh || true
        if [[ "${SUPPRESS_STAGE_FAILURE_ROWS:-0}" == "1" ]]; then
          echo "Suppressed failure row for retry attempt: ${REASON}"
          return 22
        fi
        append_stage_failure_rows "$CONTEXTS" "$CONCURRENCIES" "$REASON" "$STAGE_MAX_NEW_TOKENS" "$STAGE_NUM_REQUESTS"
        echo "Completed failed stage ${STAGE_NAME}."
        return 0
      fi
    else
      echo "WARNING: No KV-cache window size found in ${STAGE_LOG}; proceeding, but diagnostics may be less precise."
    fi
  fi

  curl --max-time 10 --connect-timeout 2 -sS "http://localhost:${PORT}/v1/models" \
    > "${METRICS_DIR}/models_${STAGE_NAME}.json" || true

  MODEL_NAME="$MODEL_NAME" \
  PLAN_MODEL="$MODEL_PATH" \
  TOKENIZER_PATH="$MODEL_PATH" \
  PROMPT_TOKEN_RESERVE="$PROMPT_TOKEN_RESERVE" \
  DECODE_MODE="$DECODE_MODE" \
  QUANTIZATION="$QUANTIZATION" \
  OUT="$OUT" \
  CONTEXTS="$CONTEXTS" \
  CONCURRENCIES="$CONCURRENCIES" \
  SERVER_MAX_SEQ_LEN="$SERVER_SEQ_LEN" \
  SERVER_MAX_NUM_TOKENS="$SERVER_NUM_TOKENS" \
  MAX_NEW_TOKENS="$STAGE_MAX_NEW_TOKENS" \
  NUM_REQUESTS="$STAGE_NUM_REQUESTS" \
  SAFETY_TOKENS="$SAFETY_TOKENS" \
  TP_SIZE="$TP_SIZE" \
  KV_DTYPE="$KV_DTYPE" \
  KV_MEMORY_FRACTION="$KV_FRAC" \
  TIMEOUT_S="$TIMEOUT_S" \
  FIRST_TOKEN_TIMEOUT_S="$STAGE_FIRST_TOKEN_TIMEOUT_S" \
  REQUEST_READ_TIMEOUT_S="$STAGE_FIRST_TOKEN_TIMEOUT_S" \
  OPENAI_API_MODE="$STAGE_OPENAI_API_MODE" \
  CASE_TIMEOUT_S="$CASE_TIMEOUT_S" \
  SERVER_LOG="$STAGE_LOG" \
  PLAN_OUT="$STAGE_OUT_PLAN" \
  STOP_ON_CASE_FAILURE=1 \
  bash scripts/run_benchmark_grid.sh || STAGE_STATUS=$?

  if [[ "$STAGE_STATUS" != "0" ]]; then
    echo "WARNING: Stage ${STAGE_NAME} had benchmark failure/timeout status ${STAGE_STATUS}. Diagnostics captured; continuing to next assignment stage."
    SERVER_LOG="$STAGE_LOG" bash scripts/diagnose_server.sh || true
  fi

  curl -m 20 --connect-timeout 3 -sS "http://localhost:${PORT}/metrics" \
    -o "$STAGE_METRICS" || true

  bash scripts/stop_trtllm_server.sh || true
  echo "Completed stage ${STAGE_NAME}."
}



# Wrapper around run_stage_once. For the 128K assignment case, a single KV-cache
# fraction is risky: too large can OOM during TensorRT-LLM executor creation,
# while too small can start but allocate a KV window smaller than the 128K prompt.
# Therefore long_128k may pass a space-separated KV ladder, e.g.
#   "0.75 0.70 0.65 0.60 0.55 0.50 0.45"
# The wrapper tries higher KV reservations first, validates the actual KV window
# from the server log, and records one clean failure row only after all attempts fail.
run_stage() {
  local STAGE_NAME="$1"
  local SERVER_SEQ_LEN="$2"
  local SERVER_NUM_TOKENS="$3"
  local CONTEXTS="$4"
  local CONCURRENCIES="$5"
  local MAX_BATCH_SIZE_STAGE="$6"
  local CUDA_BATCHES="$7"
  local KV_FRAC_ARG="$8"
  local ENABLE_CHUNKED_PREFILL_STAGE="$9"
  local STAGE_NUM_REQUESTS="${10:-$NUM_REQUESTS}"
  local STAGE_MAX_NEW_TOKENS="${11:-$MAX_NEW_TOKENS}"

  # Only long_128k uses the retry ladder by default. Other stages should keep
  # their tuned KV values so repeated successful 1K/8K/32K/64K runs remain stable.
  if [[ "$STAGE_NAME" == "long_128k" && "$KV_FRAC_ARG" == *" "* ]]; then
    local FINAL_REASON="server_start_failed_oom_all_kv_attempts"
    local KV_ATTEMPT
    echo "============================================================"
    echo "128K KV retry ladder enabled: ${KV_FRAC_ARG}"
    echo "The runner will only send the 128K request if the server log shows"
    echo "a KV-cache window >= context + max_new_tokens + safety tokens."
    echo "============================================================"

    for KV_ATTEMPT in $KV_FRAC_ARG; do
      echo "============================================================"
      echo "Trying 128K stage with KV memory fraction: ${KV_ATTEMPT}"
      echo "============================================================"
      # Do not append CSV failure rows for intermediate attempts. run_stage_once
      # will return a non-zero code for server startup/limit/KV-window failures.
      if SUPPRESS_STAGE_FAILURE_ROWS=1 REQUIRE_KV_WINDOW_CHECK=1 run_stage_once \
          "$STAGE_NAME" "$SERVER_SEQ_LEN" "$SERVER_NUM_TOKENS" "$CONTEXTS" "$CONCURRENCIES" \
          "$MAX_BATCH_SIZE_STAGE" "$CUDA_BATCHES" "$KV_ATTEMPT" "$ENABLE_CHUNKED_PREFILL_STAGE" \
          "${10:-$NUM_REQUESTS}" "${11:-$MAX_NEW_TOKENS}" "${12:-${FIRST_TOKEN_TIMEOUT_S:-$TIMEOUT_S}}" "${13:-${OPENAI_API_MODE:-chat}}"; then
        echo "128K stage completed using KV memory fraction ${KV_ATTEMPT}."
        return 0
      fi

      local ATTEMPT_LOG="${LOG_DIR}/server_${STAGE_NAME}.log"
      FINAL_REASON="$(classify_server_failure "$ATTEMPT_LOG")"
      if [[ -f "$ATTEMPT_LOG" ]]; then
        if grep -qiE "kv_cache_window_too_small|window size=.*required|FAIL: KV-cache window too small" "$ATTEMPT_LOG"; then
          FINAL_REASON="kv_cache_window_too_small_after_successful_start_required_${SERVER_NUM_TOKENS}"
          # Lower KV fractions will not increase the KV window, so stop trying.
          break
        fi
        # If the log shows a too-small window, lower fractions cannot help.
        local WINDOW_LINE
        WINDOW_LINE=$(grep -E "window size=[0-9]+" "$ATTEMPT_LOG" | tail -1 || true)
        if [[ -n "$WINDOW_LINE" ]]; then
          local WINDOW
          WINDOW=$(echo "$WINDOW_LINE" | sed -E 's/.*window size=([0-9]+).*/\1/')
          local REQUIRED=$(( $(echo "$CONTEXTS" | awk '{print $1}') + STAGE_MAX_NEW_TOKENS + SAFETY_TOKENS ))
          if [[ "$WINDOW" =~ ^[0-9]+$ ]] && (( WINDOW < REQUIRED )); then
            FINAL_REASON="kv_cache_window_too_small_window_${WINDOW}_required_${REQUIRED}"
            break
          fi
        fi
      fi
      echo "128K attempt with KV=${KV_ATTEMPT} did not complete. Reason so far: ${FINAL_REASON}. Trying next value if available."
    done

    echo "WARNING: All 128K KV retry attempts failed. Final reason: ${FINAL_REASON}. Recording one failure row."
    SERVER_LOG="${LOG_DIR}/server_${STAGE_NAME}.log" bash scripts/diagnose_server.sh || true
    append_stage_failure_rows "$CONTEXTS" "$CONCURRENCIES" "$FINAL_REASON" "$STAGE_MAX_NEW_TOKENS" "$STAGE_NUM_REQUESTS"
    bash scripts/stop_trtllm_server.sh || true
    echo "Completed failed stage ${STAGE_NAME}."
    return 0
  fi

  run_stage_once "$@"
}

# Stage sizing notes:
# - context_len below is the assignment scenario and is not reduced.
# - server seq/token caps are internal TensorRT-LLM capacity knobs.
# - long stages reduce max_batch_size, CUDA graph batch sizes, and KV-cache reservation
#   to avoid GPU0/rank0 executor creation OOM.

if [[ "$STRICT_SERVER_LIMITS" == "1" ]]; then
  SHORT_SEQ=4096;    SHORT_TOK=16384
  MED_SEQ=16384;     MED_TOK=65536
  LONG32_SEQ=65536;  LONG32_TOK=131072
  LONG64_SEQ=131072; LONG64_TOK=196608
  LONG128_SEQ=262144; LONG128_TOK=262144
else
  SHORT_SEQ="${SHORT_SEQ:-4096}";        SHORT_TOK="${SHORT_TOK:-16384}"
  MED_SEQ="${MED_SEQ:-16384}";           MED_TOK="${MED_TOK:-65536}"
  LONG32_SEQ="${LONG32_SEQ:-49152}";     LONG32_TOK="${LONG32_TOK:-49152}"
  LONG64_SEQ="${LONG64_SEQ:-81920}";     LONG64_TOK="${LONG64_TOK:-81920}"
  LONG128_SEQ="${LONG128_SEQ:-132352}";  LONG128_TOK="${LONG128_TOK:-132352}"
fi

SHORT_KV="${SHORT_KV:-0.70}"
MED_KV="${MED_KV:-0.60}"
LONG32_KV="${LONG32_KV:-0.30}"
LONG64_KV="${LONG64_KV:-0.45}"
LONG128_KV_LADDER="${LONG128_KV_LADDER:-0.75 0.70 0.65 0.60 0.55 0.50 0.45}"
LONG128_KV="${LONG128_KV:-$LONG128_KV_LADDER}"

# KV cache sizing note:
# Earlier 64k runs with LONG64_KV=0.18 initialized but only allocated a KV window
# around 33k tokens, causing default_max_tokens < 0 and a no-progress stall.
# These defaults reserve enough KV cache for the assignment long-context prompts.
# For 128K, the runner uses a KV retry ladder and validates the actual KV-cache
# window before sending the request. If high KV fractions OOM and lower fractions
# produce a too-small KV window, the script records a clean scalability-limit row.

SHORT_BATCH="${SHORT_BATCH:-8}"
MED_BATCH="${MED_BATCH:-4}"
LONG_BATCH="${LONG_BATCH:-1}"

SHORT_CUDA_BATCHES="${SHORT_CUDA_BATCHES:-1 2 4 8}"
MED_CUDA_BATCHES="${MED_CUDA_BATCHES:-1 2 4}"
LONG_CUDA_BATCHES="${LONG_CUDA_BATCHES:-1}"

SHORT_CHUNKED_PREFILL="${SHORT_CHUNKED_PREFILL:-false}"
MED_CHUNKED_PREFILL="${MED_CHUNKED_PREFILL:-false}"
LONG_CHUNKED_PREFILL="${LONG_CHUNKED_PREFILL:-false}"
# Set LONG_CHUNKED_PREFILL=true only for experiments; for Qwen3-Coder-480B NVFP4 on TRT-LLM 1.1.0,
# chunked prefill can accept a 64k request but stall with default_max_tokens < 0.

LONG32_CONCURRENCIES="1"
if [[ "$INCLUDE_32K_CONCURRENCY2" == "1" ]]; then
  LONG32_CONCURRENCIES="1 2"
fi

# Stage-specific workload sizes. These keep the assignment input context lengths
# unchanged, but avoid running 8 repeated 64k/128k requests with long generation.
SHORT_NUM_REQUESTS="${SHORT_NUM_REQUESTS:-8}"
MED_NUM_REQUESTS="${MED_NUM_REQUESTS:-8}"
LONG32_NUM_REQUESTS="${LONG32_NUM_REQUESTS:-4}"
LONG64_NUM_REQUESTS="${LONG64_NUM_REQUESTS:-1}"
LONG128_NUM_REQUESTS="${LONG128_NUM_REQUESTS:-1}"

SHORT_MAX_NEW_TOKENS="${SHORT_MAX_NEW_TOKENS:-256}"
MED_MAX_NEW_TOKENS="${MED_MAX_NEW_TOKENS:-256}"
LONG32_MAX_NEW_TOKENS="${LONG32_MAX_NEW_TOKENS:-128}"
LONG64_MAX_NEW_TOKENS="${LONG64_MAX_NEW_TOKENS:-64}"
# 128k uses the minimum viable generation budget to test the assignment context
# while avoiding extra executor/KV-estimation memory. Increase manually only if it starts cleanly.
LONG128_MAX_NEW_TOKENS="${LONG128_MAX_NEW_TOKENS:-1}"

SHORT_API_MODE="${SHORT_API_MODE:-chat}"
MED_API_MODE="${MED_API_MODE:-chat}"
LONG32_API_MODE="${LONG32_API_MODE:-chat}"
LONG64_API_MODE="${LONG64_API_MODE:-completion}"
LONG128_API_MODE="${LONG128_API_MODE:-completion}"

